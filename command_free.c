#include "minishell.h"

void		free_first_cmd(char ***str, int i)
{
	int		a;

	if (!str || !*str || !(*str)[1] || !(*str)[2])
		return ;
	free((*str)[0]);
	free((*str)[1]);
	a = 0;
	i = 2;
	while ((*str)[i])
	{
		(*str)[a] = (*str)[i];
		a++;
		i++;
	}
	(*str)[a] = NULL;
}
